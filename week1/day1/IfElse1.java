public class IfElse1{
	public static void main(String[] args) {
		int number=0;
		if (number>0) {
			System.out.println("Positive number:"+number);
		} 
		else if (number==0) {
		    System.out.println("Neutral number:"+number);
		}
		else {
			System.out.println("negative number:"+number);
		}
		}
}
